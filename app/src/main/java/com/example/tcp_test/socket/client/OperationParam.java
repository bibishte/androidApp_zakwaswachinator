package com.example.tcp_test.socket.client;

public class OperationParam {
    private float zadanie;
    private float kp;
    private float kid;
    private float limit;
    private float kd;
    private float timeStart;
    private float timeStop;
    private float time;
    private boolean rele1On;
    private boolean rele3On;

    public float getZadanie() {
        return zadanie;
    }

    public void setZadanie(float zadanie) {
        this.zadanie = zadanie;
    }

    public float getKp() {
        return kp;
    }

    public void setKp(float kp) {
        this.kp = kp;
    }

    public float getKid() {
        return kid;
    }

    public void setKid(float kid) {
        this.kid = kid;
    }

    public float getLimit() {
        return limit;
    }

    public void setLimit(float limit) {
        this.limit = limit;
    }

    public float getKd() {
        return kd;
    }

    public void setKd(float kd) {
        this.kd = kd;
    }

    public float getTimeStart() {
        return timeStart;
    }

    public void setTimeStart(float timeStart) {
        this.timeStart = timeStart;
    }

    public float getTimeStop() {
        return timeStop;
    }

    public void setTimeStop(float timeStop) {
        this.timeStop = timeStop;
    }

    public float getTime() {
        return time;
    }

    public void setTime(float time) {
        this.time = time;
    }

    public boolean isRele1On() {
        return rele1On;
    }

    public void setRele1On(boolean rele1On) {
        this.rele1On = rele1On;
    }

    public boolean isRele3On() {
        return rele3On;
    }

    public void setRele3On(boolean rele3On) {
        this.rele3On = rele3On;
    }
}
